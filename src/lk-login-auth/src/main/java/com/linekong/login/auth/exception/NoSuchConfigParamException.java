package com.linekong.login.auth.exception;

public class NoSuchConfigParamException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2053859800989667764L;

	public NoSuchConfigParamException() {
		super();
	}

	public NoSuchConfigParamException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoSuchConfigParamException(String message) {
		super(message);
	}

	public NoSuchConfigParamException(Throwable cause) {
		super(cause);
	}
	
}
