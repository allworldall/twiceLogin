package com.linekong.login.auth.exception;

public class NoSuchRequestParamException extends Exception {


	/**
	 * 
	 */
	private static final long serialVersionUID = -6179912913084484260L;

	public NoSuchRequestParamException() {
		super();
	}

	public NoSuchRequestParamException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoSuchRequestParamException(String message) {
		super(message);
	}

	public NoSuchRequestParamException(Throwable cause) {
		super(cause);
	}
	
}
